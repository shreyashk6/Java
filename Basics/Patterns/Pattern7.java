package Patterns;
public class Pattern7 {
    public static void main(String[] args) {
        int n = 5;
        for(int i = 1;i<n+1;i++){
            for(int j=1;j<=n;j++){

            
                System.out.print((i+j)+" ");

            }
            System.out.println();
           
            
        }
    }
}
