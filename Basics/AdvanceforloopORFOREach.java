public class AdvanceforloopORFOREach {
    //This Loop to traverse only collection of data
    //This loop doesnt not require any starting or ending point to traaverse the data.
    //This loop will iterate as many times as data present in the collection.
    public static void main(String[] args) {
        int arr []  = {1,2,3,4,5,6,7,8,9,0};
        for (int n : arr) {
            System.out.println(n);
        }
    }
}
